inline void gpio_dir(int port, int pin, unsigned int dir)
{
    Chip_GPIO_SetPinDIR(LPC_GPIO, port, pin, dir);
}

inline void gpio_set(int port, int pin, unsigned int value)
{
    Chip_GPIO_SetPinState(LPC_GPIO, port, pin, value);
}

inline uint gpio_get(int port, int pin)
{
    return Chip_GPIO_GetPinState(LPC_GPIO, port, pin);
}
